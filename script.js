function setColorRed() {
    document.getElementsByClassName("yellow")[0].style.background = "grey";
    document.getElementsByClassName("red")[0].style.background = "red";
    document.getElementsByClassName("green")[0].style.background = "grey";
}



function setColorYellow() {
    document.getElementsByClassName("yellow")[0].style.background = "yellow";
    document.getElementsByClassName("red")[0].style.background = "grey";
    document.getElementsByClassName("green")[0].style.background = "grey";
}

function setColorGreen() {
    document.getElementsByClassName("yellow")[0].style.background = "grey";
    document.getElementsByClassName("red")[0].style.background = "grey";
    document.getElementsByClassName("green")[0].style.background = "green";
}